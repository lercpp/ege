p={
    (' ',0):(' ',-1,1),
    (' ',1):(' ',2,1),
    (' ',2):(' ',2,2),
    (' ',3):(' ',2,3),
    ('1',1):('0',-1,3),
    ('1',2):('0',-1,1),
    ('1',3):('1',-1,2),
    ('0',1):('1',-1,2),
    ('0',2):('0',-1,3),
    ('0',3):('1',-1,1)
}

def mt(s):
    s=list(' '+s+' ')
    i=-1
    q=0
    while True:
        cmd = p[(s[i],q)]
        s[i]=cmd[0]
        if cmd[1]==2:break
        i+=cmd[1]
        q=cmd[2]
    return ''.join(s)
